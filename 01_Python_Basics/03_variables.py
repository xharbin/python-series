# variables: a container for storing data values.

x = 5
y = "john"

print(x)
print(y)

# Name variable

name = "harry"
print(f"My name is {name}.")

# Many values to multiple variables

x, y, z = "orange", "banana", "apple"
print(x)
print(y)
print(z)

# one value to multiple variables
x = y = z = "apple"
print(x)
print(y)
print(z)
