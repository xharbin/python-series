# strings: strings are either quoted in single or double quotation.

print("It's double quotation strings!")
print('Its single quotation strings!')


# Quotes inside quotes:

print("It's alright!")
print("He is called 'Jonny'")           # single quotation in strings
print('He is called "Joony"')           # double quotation in strings


#  Assigning strings in variable:

a = " hello bro!"
print(a)

number = "12345"                  # when it is inside the quotation, it's considered as strings
print(number)


