# Escape character = a backslash \ followed by the character you want to insert.

txt = "Hello, My name is \"Mark\" . I am \"20\" years old"

print(txt)


# \n

txt = "Hello! My name is Mark. \n I am 20 years old."

print(txt)


# \t

txt = "Hello! My name is Mark. \t I am 20 years old."

print(txt)