# Assignment operators are used to assign values to variables

# 
x = 5
print(x)

#  += : 
a = 5
a += 3
print(a)


# -= :
b = 5
b -= 3
print(b)


# *= :
c = 3
c *= 3
print(c)


# /= :
d = 55
d /= 5
print(d)


# //= :
e = 43
e //= 3
print(e)


# **= :
f = 3
f **= 3
print(f)


# %= :
g = 65
g %= 6
print(g)