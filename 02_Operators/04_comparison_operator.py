# Comparison Operator are used to compare two values.

# Equal to: ==
x = 5
y = 5

print(x == y)


# Not equal to: !=
i = 7
j = 7

print(i != j)


# Greater than: >
k = 5
l = 7

print(k > l)


# Less than: <
num1 = 3
num2 = 5

print(num1 < num2)


# Greater than or equal to: >=
num3 = 8
num4 = 9

print(num3 >= num4)


# Less than or equal to: <=
num5 = 5
num6 = 8

print(num5 <= num6)