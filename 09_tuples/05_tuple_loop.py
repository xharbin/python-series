# Using 'for' loop:
fruits = ('apple', 'banana', 'orange', 'grapes', 'berry')

for x in fruits:
    print(x)


fruits1 = ('apple', 'banana', 'orange', 'grapes', 'berry')

for i in range(len(fruits1)):
    print(fruits1[i])


# Using 'while' loop:
fruits3 = ('apple', 'banana', 'orange', 'grapes', 'berry')
i = 0

while i < len(fruits3):
    print(fruits3[i])
    i += 1


