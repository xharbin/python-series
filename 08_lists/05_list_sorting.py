# sort list alphabetically (a-z)
list1 = ['orange', 'apple', 'berry', 'grapes', 'watermelon']

list1.sort()
print(list1)


# sort list numerically (1-...)
list2 = [34, 55, 22, 65, 42, 11]

list2.sort()
print(list2)


# Descending Order:
# sort list in descending (z-a)
list_a = ['orange', 'apple', 'berry', 'grapes', 'watermelon']

list_a.sort(reverse=True)
print(list_a)


# sort list in descending (1 - ...)
list_b = [34, 55, 22, 65, 42, 11]

list_b.sort(reverse=True)
print(list_b)


thislist = ["banana", "Orange", "Kiwi", "cherry"]
thislist.reverse()
print(thislist)